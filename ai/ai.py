import os, requests

# 🔧 Відповіді від AI (DeepSeek)
def ai_answer(text):
    try:
        response = requests.post(
            "https://api.deepseek.com/v1/chat/completions",
            headers={"Authorization": "Bearer " + os.getenv("DEEPSEEK_API_KEY")},
            json={
                "model": "deepseek-chat",
                "messages": [{"role": "user", "content": text}]
            }
        )
        data = response.json()
        if "choices" in data and len(data["choices"]) > 0:
            return "💬 Професійна відповідь:\n" + data["choices"][0]["message"]["content"]
        else:
            return (
                "⚠️ Тимчасово немає відповіді від AI.\n"
                "Але ми вже готові допомогти вам особисто!\n"
                "📞 Телефон: +380671902929\n"
                "🌐 Сайт: https://whitemedia.com.ua\n"
                "Замовте консультацію й отримайте рішення для вашого бізнесу."
            )
    except Exception:
        return (
            "⚠️ Немає відповіді від моделі.\n"
            "Зверніться до нашого менеджера:\n"
            "📞 +380671902929\n🌐 https://whitemedia.com.ua"
        )

